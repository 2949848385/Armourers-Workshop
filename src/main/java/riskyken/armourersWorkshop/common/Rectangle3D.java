package riskyken.armourersWorkshop.common;

public class Rectangle3D {

    public int x;
    public int y;
    public int z;
    public int width;
    public int height;
    public int depth;
    
    public Rectangle3D(int x, int y, int z, int width, int height, int depth) {
        this.x = x;
        this.y = y;
        this.z = z;
        this.width = width;
        this.height = height;
        this.depth = depth;
    }
}
